export function calculatePathScores(answers: string[]): Record<Path, number> {
  const scores: Record<Path, number> = { critical: 0, constructive: 0, deconstructive: 0, perceptive: 0 };
  for (const answerId of answers) {
    const option = findOption(answerId);
    if (option) scores[option.path]++;
  }
  return scores;
}

export function calculateDimensionScores(answers: string[]): Record<Dimension, Record<Path, number>> {
  const dimScores: Record<Dimension, Record<Path, number>> = {
    world: { critical: 0, constructive: 0, deconstructive: 0, perceptive: 0 },
    tech: { critical: 0, constructive: 0, deconstructive: 0, perceptive: 0 },
    community: { critical: 0, constructive: 0, deconstructive: 0, perceptive: 0 },
    ecology: { critical: 0, constructive: 0, deconstructive: 0, perceptive: 0 }
  };
  for (const answerId of answers) {
    const option = findOption(answerId);
    const question = findQuestionByOption(answerId);
    if (option && question) dimScores[question.dimension][option.path]++;
  }
  return dimScores;
}

/** 余弦相似度：计算用户与思想家维度画像的匹配程度 */
function cosineSimilarity(a: Record<Dimension, number>, b: Record<Dimension, number>): number {
  const dims = ['world', 'tech', 'community', 'ecology'] as Dimension[];
  let dotProduct = 0, normA = 0, normB = 0;
  for (const dim of dims) {
    dotProduct += a[dim] * b[dim];
    normA += a[dim] ** 2;
    normB += b[dim] ** 2;
  }
  if (normA === 0 || normB === 0) return 0;
  return dotProduct / (Math.sqrt(normA) * Math.sqrt(normB));
}

function normalizeTitle(title: string): string {
  // 移除标点和空格，用于比较书名是否重复
  return title.replace(/[，。、：:；;！!？?（）()\s\-—]/g, '').toLowerCase();
}

