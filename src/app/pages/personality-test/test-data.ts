function getDimensionScoreTotals(dimScores: Record<Dimension, Record<Path, number>>): Record<Dimension, number> {
  const result: Record<Dimension, number> = { world: 0, tech: 0, community: 0, ecology: 0 };
  for (const dim of Object.keys(dimScores) as Dimension[]) {
    result[dim] = Object.values(dimScores[dim]).reduce((a, b) => a + b, 0);
  }
  return result;
}

