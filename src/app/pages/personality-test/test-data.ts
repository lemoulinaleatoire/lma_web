function findOption(answerId: string): TestOption | undefined {
  for (const q of QUESTIONS) {
    const found = q.options.find(o => o.id === answerId);
    if (found) return found;
  }
  return undefined;
}

function findQuestionByOption(answerId: string): TestQuestion | undefined {
  return QUESTIONS.find(q => q.options.some(o => o.id === answerId));
}

export function encodeAnswers(answers: string[]): string {
  return answers.join(',');
}

export function decodeAnswers(encoded: string): string[] {
  return encoded.split(',').map(s => s.trim()).filter(s => s.length > 0);
}
